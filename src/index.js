function Pizza({ pizzaObject }) {
  // console.log(props);
  return (
    // creating a list item with the object properties from the original array plus the parent array
    <li className={`pizza ${pizzaObject.soldOut ? "sold-out" : ""}`}>
      <img src={pizzaObject.photoName} alt={pizzaObject.name} />
      <div>
        <h3>{pizzaObject.name}</h3>
        <p>{pizzaObject.ingredients}</p>
        <span>{pizzaObject.soldOut ? "SOLD OUT" : pizzaObject.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;
  console.log(isOpen);

  // if (hour >= openHour && hour <= closeHour) alert("We're Currently Open!");
  // else alert("sorry we're closed...");

  return (
    // using conditional rendering via termary operator (?) to display a conditional statement
    <footer className="footer">
      {isOpen ? (
        <Order closeHour={closeHour} openHour={openHour} />
      ) : (
        <p> We are currently closed. We open at {openHour}:00 am</p>
      )}
    </footer>
  );
  // return React.createElement("footer", null, "We're currently Open");
}

function Order({ closeHour, openHour }) {
  return (
    <div className="order">
      <p>
        We're open from {openHour}:00 to {closeHour}:00. Dine in or order
        online!
      </p>
      <button className="btn">Order</button>
    </div>
  );
}

